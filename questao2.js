/*
2. O que retorna a expressão (true || false) && (false || true)?

(true || false) avalia para true, pois pelo menos um dos operandos é verdadeiro.
(false || true) também avalia para true, pelo mesmo motivo.
Em seguida, a expressão é avaliada como true && true, o que resulta em true, pois ambos os operandos são verdadeiros.
*/